def parse_devices():
    """Parse device data from tab-separated format"""
    devices = []
    lines = devices_data.strip().split('\n')
    
    for line in lines:
        parts = line.split('\t')
        if len(parts) >= 3:
            device_id = parts[0].strip()
            locationcode = parts[1].strip()
            city = parts[2].strip() if len(parts) > 2 else ""
            sn_pc = parts[3].strip() if len(parts) > 3 else ""
            sn_sc = parts[4].strip() if len(parts) > 4 else ""
            
            # Skip empty device IDs
            if not device_id or device_id == 'need to connect':
                continue
            
            device = {
                "device_id": device_id,
                "tenant_id": TENANT_ID,
                "locationcode": locationcode,
                "city": city,
                "sn_pc": sn_pc,
                "sn_sc": sn_sc,
                "status": "online",
                "country": "Deutschland",
                "created_at": datetime.utcnow().isoformat(),
                "updated_at": datetime.utcnow().isoformat()
            }
            
            devices.append(device)
    
    return devices

async def import_devices():
    """Import devices directly to MongoDB"""
    client = AsyncIOMotorClient(MONGO_URL)
    db = client[DB_NAME]
    collection = db.devices
    
    devices = parse_devices()
    
    print(f"📦 Gefundene Geräte: {len(devices)}")
    print(f"🎯 Tenant ID: {TENANT_ID}")
    print(f"🔗 MongoDB URL: {MONGO_URL}")
    print(f"📊 Database: {DB_NAME}")
    print()
    
    success_count = 0
    error_count = 0
    skipped_count = 0
    
    for idx, device in enumerate(devices, 1):
        try:
            # Check if device already exists
            existing = await collection.find_one({"device_id": device['device_id'], "tenant_id": TENANT_ID})
            
            if existing:
                print(f"⏭️  [{idx}/{len(devices)}] Gerät {device['device_id']} existiert bereits - übersprungen")
                skipped_count += 1
                continue
            
            # Insert device
            result = await collection.insert_one(device)
            
            if result.inserted_id:
                print(f"✅ [{idx}/{len(devices)}] Gerät {device['device_id']} erfolgreich erstellt")
                success_count += 1
            else:
                print(f"❌ [{idx}/{len(devices)}] Fehler bei {device['device_id']}")
                error_count += 1
                
        except Exception as e:
            print(f"❌ [{idx}/{len(devices)}] Exception bei {device['device_id']}: {str(e)}")
            error_count += 1
    
    print()
    print("="*60)
    print(f"✅ Erfolgreich importiert: {success_count}")
    print(f"⏭️  Übersprungen (existieren bereits): {skipped_count}")
    print(f"❌ Fehler: {error_count}")
    print(f"📊 Gesamt: {len(devices)}")
    print("="*60)
    
    client.close()

if __name__ == "__main__":
    asyncio.run(import_devices())
