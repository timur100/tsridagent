def extract_device_number(location_code):
    """Extract device number from location code (last 2 digits)"""
    if len(location_code) >= 2 and location_code[-2:].isdigit():
        return location_code[-2:]
    return "01"


async def import_locations():
    """Parse TSV and import all locations into MongoDB"""
    
    client = AsyncIOMotorClient(MONGO_URL)
    db = client[DB_NAME]
    locations_collection = db['locations']
    
    try:
        # Test connection
        await client.server_info()
        print("✓ Connected to MongoDB\n")
        
        # Parse TSV data
        lines = TSV_DATA.strip().split('\n')
        
        locations = []
        for i, line in enumerate(lines[1:], 1):  # Skip header
            if not line.strip():
                continue
                
            fields = line.split('\t')
            if len(fields) < 9:
                print(f"⚠️  Skipping line {i}: insufficient fields")
                continue
            
            location_code = fields[0].strip()
            state_abbr = fields[9].strip() if len(fields) > 9 else ""
            state_full = STATE_MAPPING.get(state_abbr, state_abbr)
            
            location = {
                "locationCode": location_code,
                "deviceNumber": extract_device_number(location_code),
                "locationName": fields[1].strip(),
                "street": fields[2].strip(),
                "zip": fields[3].strip(),
                "city": fields[4].strip(),
                "state": state_full,
                "country": fields[5].strip(),
                "continent": "Europe",  # All are in Germany/Europe
                "phone": fields[6].strip(),
                "email": fields[7].strip(),
                "manager": fields[8].strip() if len(fields) > 8 else "",
                "tvid": "",  # Can be added later
                "snStation": "",  # Can be added later
                "snScanner": ""  # Can be added later
            }
            
            locations.append(location)
        
        print(f"✓ Parsed {len(locations)} locations from TSV data\n")
        
        # Clear existing data
        existing_count = await locations_collection.count_documents({})
        if existing_count > 0:
            print(f"⚠️  Found {existing_count} existing locations in database")
            response = input("Clear existing data and import real locations? (y/N): ")
            if response.lower() != 'y':
                print("Import cancelled")
                return
            
            await locations_collection.delete_many({})
            print("✓ Cleared existing locations\n")
        
        # Import new data in batches
        batch_size = 50
        print(f"Importing {len(locations)} locations in batches of {batch_size}...")
        
        for i in range(0, len(locations), batch_size):
            batch = locations[i:i + batch_size]
            await locations_collection.insert_many(batch)
            print(f"  ✓ Imported batch {i//batch_size + 1}: {len(batch)} locations")
        
        print(f"\n✅ Successfully imported {len(locations)} real locations!\n")
        
        # Show statistics
        total = await locations_collection.count_documents({})
        continents = await locations_collection.distinct("continent")
        countries = await locations_collection.distinct("country")
        states = await locations_collection.distinct("state")
        cities = await locations_collection.distinct("city")
        
        print("=" * 60)
        print("📊 Database Statistics:")
        print("=" * 60)
        print(f"  Total Locations: {total}")
        print(f"  Continents: {len(continents)} ({', '.join(continents)})")
        print(f"  Countries: {len(countries)} ({', '.join(countries)})")
        print(f"  Federal States: {len(states)}")
        print(f"  Cities: {len(cities)}")
        print("=" * 60)
        
        # Show sample locations by state
        print("\n📍 Sample Locations by State:")
        for state in sorted(states)[:5]:  # Show first 5 states
            sample = await locations_collection.find_one({"state": state})
            if sample:
                print(f"  {state}: {sample['locationCode']} - {sample['locationName']}, {sample['city']}")
        
        print(f"\n✅ Import complete! All {total} locations are now in MongoDB.")
        print("You can now use the AdminPanel to select locations.\n")
        
    except Exception as e:
        print(f"\n✗ Error: {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)
    finally:
        client.close()


