def parse_teamviewer_data():
    """Parse the TeamViewer mapping data"""
    mapping = {}
    for line in teamviewer_mapping.strip().split('\n'):
        if line.strip():
            parts = line.split('\t')
            if len(parts) == 2:
                device_identifier = parts[0].strip()
                tvid = parts[1].strip()
                
                # Store in mapping - for duplicates, create a list
                if device_identifier in mapping:
                    if isinstance(mapping[device_identifier], list):
                        mapping[device_identifier].append(tvid)
                    else:
                        mapping[device_identifier] = [mapping[device_identifier], tvid]
                else:
                    mapping[device_identifier] = tvid
    
    return mapping

