    for device in devices:
        device_id = device.get('device_id', '')
        locationcode = device.get('locationcode', '')
        
        # Try to match by device_id first, then by locationcode
        tvid = None
        match_key = None
        
        # Strategy 1: Exact match on device_id
        if device_id in mapping:
            tvid = mapping[device_id]
            match_key = f"exact:{device_id}"
        
        # Strategy 2: Match with any key that starts with device_id
        # (e.g., "AGBC02-01" matches "AGBC02-01-AUGSBURG")
        if not tvid:
            for key in mapping.keys():
                if key.startswith(device_id + '-') or key.startswith(device_id):
                    tvid = mapping[key]
                    match_key = f"prefix:{key}"
                    break
        
        # Strategy 3: Match locationcode-XX pattern
        # (e.g., "BERL01-01" matches keys starting with "BERL01-01")
        if not tvid and device_id:
            for key in mapping.keys():
                # Remove any suffix after device_id and try matching
                if device_id in key:
                    tvid = mapping[key]
                    match_key = f"contains:{key}"
                    break
        
        # Strategy 4: Try locationcode exact match
        if not tvid and locationcode in mapping:
            tvid = mapping[locationcode]
            match_key = f"locationcode:{locationcode}"
        
        if tvid:
            # Handle multiple TVIDs (take first one)
            if isinstance(tvid, list):
                print(f"⚠️  Multiple TVIDs for {device_id}: {tvid} - using first one")
                tvid = tvid[0]
                multi_match_count += 1
            
            # Update device
            result = db.europcar_devices.update_one(
                {'device_id': device_id},
                {'$set': {'tvid': str(tvid)}}
            )
            
            if result.modified_count > 0:
                updated_count += 1
                print(f"✅ Updated {device_id} (matched via {match_key}) with TVID: {tvid}")
        else:
            not_found_count += 1
            print(f"❌ No TVID found for {device_id} (locationcode: {locationcode})")
    
    # Summary
    print(f"\n{'='*60}")
    print(f"📊 SUMMARY:")
    print(f"  ✅ Updated: {updated_count} devices")
    print(f"  ❌ Not found: {not_found_count} devices")
    print(f"  ⚠️  Multiple matches: {multi_match_count} devices")
    print(f"{'='*60}")
    
    return updated_count, not_found_count

